package com.center.buuluu.model;

import java.io.Serializable;

import org.codehaus.jackson.annotate.JsonProperty;

public class UserSleepDataModel implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 8102545793063122100L;

	@JsonProperty(value = "sleepEffciency")
	private Integer sleepEffciency;

	@JsonProperty(value = "turns")
	private Integer turns;

	@JsonProperty(value = "sleepHours")
	private Float sleepHours;

	@JsonProperty(value = "qualitySleepHours")
	private Float qualitySleepHours;

	@JsonProperty(value = "userId")
	private String userId;

	@JsonProperty(value = "wristbandId")
	private String wristbandId;

	@JsonProperty(value = "sleepDate")
	private String sleepDate;

	public Integer getSleepEffciency() {
		return sleepEffciency;
	}

	public void setSleepEffciency(Integer sleepEffciency) {
		this.sleepEffciency = sleepEffciency;
	}

	public Integer getTurns() {
		return turns;
	}

	public void setTurns(Integer turns) {
		this.turns = turns;
	}

	public Float getSleepHours() {
		return sleepHours;
	}

	public void setSleepHours(Float sleepHours) {
		this.sleepHours = sleepHours;
	}

	public Float getQualitySleepHours() {
		return qualitySleepHours;
	}

	public void setQualitySleepHours(Float qualitySleepHours) {
		this.qualitySleepHours = qualitySleepHours;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getWristbandId() {
		return wristbandId;
	}

	public void setWristbandId(String wristbandId) {
		this.wristbandId = wristbandId;
	}

	public String getSleepDate() {
		return sleepDate;
	}

	public void setSleepDate(String sleepDate) {
		this.sleepDate = sleepDate;
	}

}
