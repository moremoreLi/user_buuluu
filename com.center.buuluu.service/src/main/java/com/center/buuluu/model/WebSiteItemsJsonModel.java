package com.center.buuluu.model;

import java.io.Serializable;

import org.codehaus.jackson.annotate.JsonProperty;

public class WebSiteItemsJsonModel implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -7523924795140053962L;

	@JsonProperty("id")
	private String id;

	@JsonProperty("po_id")
	private String po_id;

	@JsonProperty("sku_no")
	private String sku_no;

	@JsonProperty("sku_name")
	private String sku_name;

	@JsonProperty("qty")
	private String qty;

	@JsonProperty("price")
	private String price;

	@JsonProperty("saleSum")
	private String saleSum;

	@JsonProperty("paySum")
	private String paySum;

	@JsonProperty("orderDiscount")
	private String orderDiscount;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPo_id() {
		return po_id;
	}

	public void setPo_id(String po_id) {
		this.po_id = po_id;
	}

	public String getSku_no() {
		return sku_no;
	}

	public void setSku_no(String sku_no) {
		this.sku_no = sku_no;
	}

	public String getSku_name() {
		return sku_name;
	}

	public void setSku_name(String sku_name) {
		this.sku_name = sku_name;
	}

	public String getQty() {
		return qty;
	}

	public void setQty(String qty) {
		this.qty = qty;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getSaleSum() {
		return saleSum;
	}

	public void setSaleSum(String saleSum) {
		this.saleSum = saleSum;
	}

	public String getPaySum() {
		return paySum;
	}

	public void setPaySum(String paySum) {
		this.paySum = paySum;
	}

	public String getOrderDiscount() {
		return orderDiscount;
	}

	public void setOrderDiscount(String orderDiscount) {
		this.orderDiscount = orderDiscount;
	}

}
